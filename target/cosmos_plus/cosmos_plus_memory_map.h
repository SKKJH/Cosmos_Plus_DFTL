
#ifndef __COSMOS_PLUS_MEMORY_MAP_H_
#define __COSMOS_PLUS_MEMORY_MAP_H_

#define DRAM_START_ADDR					0x00100000

#define MEMORY_SEGMENTS_START_ADDR		DRAM_START_ADDR
#define MEMORY_SEGMENTS_END_ADDR		0x001FFFFF

#define NVME_MANAGEMENT_START_ADDR		0x00200000
#define NVME_MANAGEMENT_END_ADDR		0x002FFFFF

#define RESERVED0_START_ADDR			0x00300000
#define RESERVED0_END_ADDR				0x0FFFFFFF

#define FTL_MANAGEMENT_START_ADDR		0x10000000			// FW DRAM START ADDRESS
#define FW_DATA_BUFFER_START_ADDR		FTL_MANAGEMENT_START_ADDR			// For Data buffer

#define FW_DRAM_START_ADDR				0x18000000			// Start of FW Data
#define DRAM_END_ADDR					0x3FFFFFFF

#define FW_DATA_BUFFER_SIZE				(FW_DRAM_START_ADDR - FW_DATA_BUFFER_START_ADDR)
#define FW_DRAM_SIZE					(DRAM_END_ADDR - FW_DRAM_START_ADDR);

#endif // end of #ifndef __COSMOS_PLUS_MEMORY_MAP_H_
